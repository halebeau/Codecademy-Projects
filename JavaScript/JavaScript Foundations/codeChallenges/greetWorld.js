// function declaration:
function greetWorld() {
  return "Hello, World!"
  }

// function expression assigned to a variable:
const greetWorld = function() {
  return "Hello, World!";
};

// arrow function:
const greetWorld = () => {
  return "Hello, World!";
};

// Simplified arrow function with implicit return:
const greetWorld = () => "Hello, World!";